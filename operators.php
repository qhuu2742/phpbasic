<?php

//Các toán tử đươc sử dụng để thực hiện các hoạt động trên các biến và giá trị
//PHP có các nhóm toán tử sau:
//      Toán tử số học
//      Toán tử gán
//      Toán tử so sánh
//      Toán tử tăng giảm
//      Toán tử logic
//      Toán tử chuỗi
//      Toán tử mảng
//      Toán tử gán có điều kiện

//Toán tử số học - dùng để thực hiện với các giá trị số các phép toán phổ thông + - x /
//  Operator	Name	            Example	    Result
//  +	        Cộng	            $x + $y	    Tổng $x và $y
//  -	        Trừ	                $x - $y	    Hiệu $x và $y
//  *	        Nhân	            $x * $y	    Kết quả $x và $y
//  /	        Chia	            $x / $y	    Thương số of $x và $y
//  %	        Phần trăm	        $x % $y	    Phần còn lại của $x chiacho $y
//  **	        Lũy thừa	        $x ** $y	Result of raising $x to the $y'th power

//Toán tử gán - một toán tử thông dụng, dùng dấu = để gán giá trị cho một biến bất kỳ.
//  Assignment	    Same as...	    Description
//  x = y	        x = y	        The left operand gets set to the value of the expression on the right
//  x += y	        x = x + y	    Addition
//  x -= y	        x = x - y	    Subtraction
//  x *= y	        x = x * y	    Multiplication
//  x /= y	        x = x / y	    Division
//  x %= y	        x = x % y	    Modulus

//Toán tử so sánh - dùng để so sánh hai giá trị ( số hoặc chuỗi )